(* ── Cross-implementation encrypt ── *)

let test_cross_impl_encrypt_vectors json =
  let v = json_field "cross_impl_encrypt" json in
  (* Reproduce the full pipeline *)
  let ek_v = bytes_of_hex (json_string (json_field "ek_v" v)) in
  let ek_d = bytes_of_hex (json_string (json_field "ek_d" v)) in
  let coins_v = bytes_of_hex (json_string (json_field "coins_v" v)) in
  let coins_d = bytes_of_hex (json_string (json_field "coins_d" v)) in
  let (ss_v, ct_v) = Tzel.Mlkem.encaps_derand ek_v coins_v in
  check_hex "ss_v" (json_string (json_field "ss_v" v)) ss_v;
  check_hex "ct_v" (json_string (json_field "ct_v" v)) ct_v;
  let (ss_d, ct_d) = Tzel.Mlkem.encaps_derand ek_d coins_d in
  check_hex "ss_d" (json_string (json_field "ss_d" v)) ss_d;
  check_hex "ct_d" (json_string (json_field "ct_d" v)) ct_d;
  let value = Int64.of_string (json_string (json_field "v" v)) in
  let rseed = felt_of_hex (json_string (json_field "rseed" v)) in
  let memo = bytes_of_hex (json_string (json_field "memo" v)) in
  let (nonce, encrypted_data) = Tzel.Detection.encrypt_memo ~ss_v ~v:value ~rseed ~memo in
  check_hex "nonce" (json_string (json_field "nonce" v)) nonce;
  check_hex "encrypted_data" (json_string (json_field "encrypted_data" v)) encrypted_data;
  let tag = Tzel.Detection.compute_tag ss_d in
  Alcotest.(check int) "tag" (json_int (json_field "tag" v)) tag;
  let enc : Tzel.Encoding.encrypted_note = { ct_d; tag; ct_v; nonce; encrypted_data } in
  let mch = Tzel.Encoding.compute_memo_ct_hash enc in
  check_hex "memo_ct_hash" (json_string (json_field "memo_ct_hash" v)) mch

(* ── WOTS+ ── *)

let test_wots_vectors json =
  let vectors = json_list (json_field "wots" json) in
  List.iteri (fun i v ->
    let seed = felt_of_hex (json_string (json_field "seed" v)) in
    let pub_seed = felt_of_hex (json_string (json_field "auth_pub_seed" v)) in
    let key_idx = 0 in
    let expected_leaf = json_string (json_field "leaf" v) in
    let pk = Tzel.Wots.keygen ~seed ~pub_seed ~key_idx in
    let leaf = Tzel.Wots.pk_to_leaf ~pub_seed ~key_idx pk in
    let prefix = Printf.sprintf "wots[%d]" i in
    check_hex (prefix ^ ".leaf") expected_leaf leaf;
    (try
       let sighash = felt_of_hex (json_string (json_field "sighash" v)) in
       let expected_sig = json_list (json_field "signature" v) in
       let sig_vals = Tzel.Wots.sign ~seed ~pub_seed ~key_idx sighash in
       List.iteri (fun j expected_chain ->
         check_hex (Printf.sprintf "%s.sig[%d]" prefix j)
           (json_string expected_chain) sig_vals.(j)
       ) expected_sig
     with Not_found -> ())
  ) vectors

