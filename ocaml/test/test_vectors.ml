(* Test vector validation: loads JSON vectors generated by either
   implementation and checks that our OCaml outputs match.

   WHAT THIS TESTS vs. WHAT IT CAN'T:

   Deterministic (tested via byte-exact comparison):
   - BLAKE2s with the active protocol personalizations
   - Full key hierarchy from master_sk
   - Per-address: d_j, nk_spend, nk_tag, auth_root/auth_pub_seed (depth-16 XMSS tree), owner_tag
   - ML-KEM 64-byte seed derivation
   - ML-KEM keygen_det: ek and dk bytes from seed (FIPS 203 deterministic)
   - ML-KEM encaps_derand: ss and ct from (ek, coins) (deterministic given coins)
   - ChaCha20-Poly1305 encryption (deterministic given key/nonce/plaintext)
   - Detection tag computation
   - memo_ct_hash (H_memo over encrypted note fields)
   - WOTS+ keygen, sign (from seed, no randomness)
   - Merkle roots from leaves
   - Note commitments, nullifiers
   - Sighash fold
   - Account IDs
   - Canonical wire encoding byte layout
   - Cross-implementation encrypt: full pipeline with derand encaps

   NOT testable via static vectors (malleable/randomized):
   - ML-KEM randomized encaps (different each run by design)
   - STARK proofs (verified via verify_proof, not byte comparison)
   - Any operation that uses /dev/urandom internally

   For the randomized operations, we test the PROPERTY instead:
   - encaps(ek) produces (ss, ct) such that decaps(dk, ct) == ss
   - A proof produced by tzel-prove is accepted by tzel-verify
   These are covered in test_main.ml's unit tests.
*)

let vectors_path =
  let candidates = [
    "../specs/ocaml_vectors/protocol_v1.json";
    "../../specs/ocaml_vectors/protocol_v1.json";
  ] in
  try List.find Sys.file_exists candidates
  with Not_found -> "../specs/ocaml_vectors/protocol_v1.json"

let hex_of_bytes b =
  let buf = Buffer.create (Bytes.length b * 2) in
  Bytes.iter (fun c -> Buffer.add_string buf (Printf.sprintf "%02x" (Char.code c))) b;
  Buffer.contents buf

let bytes_of_hex s = Hex.to_bytes (`Hex s)

let felt_of_hex s =
  let b = bytes_of_hex s in
  assert (Bytes.length b = 32);
  b

let json_string j = match j with `String s -> s | _ -> failwith "expected string"
let json_int j = match j with `Int n -> n | _ -> failwith "expected int"
let json_list j = match j with `List l -> l | _ -> failwith "expected list"
let json_assoc j = match j with `Assoc a -> a | _ -> failwith "expected assoc"
let json_field key j = List.assoc key (json_assoc j)

let check_hex msg expected actual_bytes =
  Alcotest.(check string) msg expected (hex_of_bytes actual_bytes)

(* ── BLAKE2s ── *)

let test_blake2s_vectors json =
  let vectors = json_list (json_field "blake2s" json) in
  List.iteri (fun i v ->
    let input = bytes_of_hex (json_string (json_field "input" v)) in
    let personal = json_string (json_field "personal" v) in
    let expected = json_string (json_field "output" v) in
    let personal_bytes = if personal = "" then Bytes.empty
      else Bytes.of_string personal in
    let actual = Tzel.Blake2s.hash ~personal:personal_bytes input in
    check_hex (Printf.sprintf "blake2s[%d]" i) expected actual
  ) vectors

