(* ── Wire encoding ── *)

let test_wire_encoding_vectors json =
  let v = json_field "wire_encoding" json in
  (* We just check the bytes are identical. The Rust side must produce
     the same encode_payment_address, encode_encrypted_note, etc. output
     for the same field values. Since the field values are deterministic
     (derived from master_sk), this is a byte-exact comparison. *)
  let expected_pa = json_string (json_field "payment_address" v) in
  let expected_enc = json_string (json_field "encrypted_note" v) in
  let expected_pn = json_string (json_field "published_note" v) in
  let expected_nm = json_string (json_field "note_memo" v) in
  (* Reconstruct from the same master_sk *)
  let master_sk = Tzel.Felt.of_u64 12345 in
  let keys = Tzel.Keys.derive master_sk in
  let addr = Tzel.Keys.derive_address keys 0 in
  let pa_wire : Tzel.Encoding.payment_address_wire = {
    d_j = addr.d_j; auth_root = addr.auth_root; auth_pub_seed = addr.auth_pub_seed; nk_tag = addr.nk_tag;
    ek_v = addr.ek_v; ek_d = addr.ek_d;
  } in
  check_hex "payment_address" expected_pa (Tzel.Encoding.encode_payment_address pa_wire);
  (* For enc/pn/nm, use the same deterministic pattern *)
  let enc : Tzel.Encoding.encrypted_note = {
    ct_d = Bytes.init 1088 (fun i -> Char.chr (i mod 256));
    tag = 42;
    ct_v = Bytes.init 1088 (fun i -> Char.chr ((i + 50) mod 256));
    nonce = Bytes.make 12 '\xAA';
    encrypted_data = Bytes.init 1080 (fun i -> Char.chr ((i + 100) mod 256));
  } in
  check_hex "encrypted_note" expected_enc (Tzel.Encoding.encode_encrypted_note enc);
  let cm = Tzel.Hash.hash_tag "wire-test-cm" in
  let pn = { Tzel.Encoding.pn_cm = cm; pn_enc = enc } in
  check_hex "published_note" expected_pn (Tzel.Encoding.encode_published_note pn);
  let nm = { Tzel.Encoding.nm_index = 42L; nm_cm = cm; nm_enc = enc } in
  check_hex "note_memo" expected_nm (Tzel.Encoding.encode_note_memo nm)

(* ── Entry point ── *)

let () =
  if not (Sys.file_exists vectors_path) then begin
    Printf.eprintf
       "No test vectors found at %s.\n\
       Generate them from the Rust implementation:\n\
       \n\
       1. From the repo root, run:\n\
          cargo run -p tzel-services --bin gen-test-vectors -- --output specs/ocaml_vectors/protocol_v1.generated.json\n\
       2. Compare or copy that file to %s\n\
       3. Re-run: dune exec test/test_vectors.exe\n\
       \n\
       Or self-check with OCaml-generated vectors:\n\
       \n\
       dune exec test/gen_vectors.exe > ../specs/ocaml_vectors/protocol_v1_ocaml.json\n\
       dune exec test/test_vectors.exe\n"
      vectors_path vectors_path;
    exit 0
  end;
  let json = Yojson.Basic.from_file vectors_path in
  let run name f =
    Alcotest.test_case name `Quick (fun () -> f json) in
  Alcotest.run "tzel-vectors" [
    "blake2s",          [ run "vectors" test_blake2s_vectors ];
    "key_hierarchy",    [ run "vectors" test_key_hierarchy_vectors ];
    "addresses",        [ run "vectors" test_address_vectors ];
    "mlkem_seeds",      [ run "vectors" test_mlkem_seed_vectors ];
    "mlkem_keygen",     [ run "vectors" test_mlkem_keygen_vectors ];
    "mlkem_encaps",     [ run "vectors" test_mlkem_encaps_derand_vectors ];
    "chacha20",         [ run "vectors" test_chacha20_vectors ];
    "detection_tags",   [ run "vectors" test_detection_tag_vectors ];
    "memo_ct_hash",     [ run "vectors" test_memo_ct_hash_vectors ];
    "cross_impl",       [ run "vectors" test_cross_impl_encrypt_vectors ];
    "wots",             [ run "vectors" test_wots_vectors ];
    "merkle",           [ run "vectors" test_merkle_vectors ];
    "notes",            [ run "vectors" test_note_vectors ];
    "sighash",          [ run "vectors" test_sighash_vectors ];
    "account_ids",      [ run "vectors" test_account_id_vectors ];
    "wire_encoding",    [ run "vectors" test_wire_encoding_vectors ];
  ]
